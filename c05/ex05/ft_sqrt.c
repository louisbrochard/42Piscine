int	ft_sqrt(int nb)
{
	int	test;
	int	i;

	test = 0;
	i = 1;
	if (nb < 0)
		return (0);
	while (i <= 46340)
	{
		test = (i * i) + ((2 * i) + 1);
		i++;
		if (test == nb)
			return (i);
	}
	return (0);
}
